#ifndef _TIME_H_
#define _TIME_H_

//************************************************************
//	クラス定義
//************************************************************
// タイムクラス
class CTime
{
public:
	// コンストラクタ
	CTime();

	// デストラクタ
	~CTime();

	// メンバ関数
	HRESULT Init(void);	// 初期化
	void Uninit(void);	// 終了
	void Update(void);	// 更新

	// 静的メンバ関数
	static CTime *Create(void);				// 生成
	static void Release(CTime *&prTime);	// 破棄

private:
	// メンバ変数
	DWORD m_dwOldTime;	// 前回の処理開始時刻
	DWORD m_dwCurTime;	// 今回の処理開始時刻
	float m_fTime;		// 処理の経過時間
};

#endif	// _TIME_H_
